import java.util.*;
public class LearningSet {
    public static void main(String[] args) {
        //stores unique values in any order
        Set<Integer> set=new HashSet<>();
        set.add(10);
        set.add(5);
        set.add(16);
        set.add(2);
        set.add(20);
        System.out.println(set);
    }   
}
