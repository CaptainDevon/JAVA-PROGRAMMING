public class Loops {
    public static void main(String[] args) {
        for(int i=0;i<5;i++)
        {
            System.out.println("For Loop");
        }

        int j=0;
        while(j<5)
        {
            System.out.println("While Loop");
            j++;
        }

        int k=0;
        do
        {
            System.out.println("Do While Loop");
            k++;
        }while (k<5);
    }
}
